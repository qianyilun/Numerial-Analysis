function solution = ASN5(x)
    A = [-3.5, -2.5, -1.5, -0.5, 0.5, 1.5, 2.5, 3.5, 4.5, 5.5];
    B = [4241691, 4290988, 4349412, 4410679, 4465924, 4499100, 4542600, 4582600, 4638400, 4683100];
    P = 0;
    
    for i = 1 : 10
        L = 1; l1 = 1; l2 = 1; 
        for j = 1:10
            if i ~= j
                l1 = l1 * ( x - A(j));
                l2 = l2 * (A(i) - A(j));
                L = l1 / l2;  
            end
        end
        
        P = L * B(i) + P;
    end
    solution = double(P);